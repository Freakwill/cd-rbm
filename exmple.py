  from cd_rbm import *  
  
  from sklearn import datasets

  digists = datasets.load_digits()
  X_train, y_train = digists.data, digists.target

  X = X_train[y_train==0]

  rbm = CDRBM(max_iter=500, mc_iter=1, persistent=False)
