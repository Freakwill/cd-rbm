  x = X[4]
  x_ = rbm.generate(mc_iter=50)

  import matplotlib.pyplot as plt
  fig = plt.figure()
  ax = fig.subplots(1, 2)
  ax[0].imshow(x.reshape((8, 8)))
  ax[0].set_title('A real digit')
  ax[1].imshow(x_.reshape((8, 8)))
  ax[1].set_title('generated by CD')
  for _ in ax: _.set_axis_off()
